list = gp = ["B2", "B4", "B6", "B8", "C2", "C7", "C10", "C11"]
list = bp = ["B1", "B3", "B7", "C1", "C4", "C5", "C6", "C8", "C9"]
list = none = ["B5", "C3", "C12"]
